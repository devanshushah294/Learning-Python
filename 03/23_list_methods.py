list_a = [6, 7, 8, 3]
# To add the element at the last of the list we need to use the append method
print(list_a)
list_a.append(25)
print(list_a)
# To add the element at the specific index of the list we need to use the append method
# list_a.insert(element, index)
list_a.insert(29, 0)
print(list_a)
